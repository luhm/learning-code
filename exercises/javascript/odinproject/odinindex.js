// Write a function called multiply that takes 2 numbers and returns their product.
function multiply(numA, numB) {
    return numA*numB
}

const multiplyResult = multiply(2, 3)

console.log (`O resultado dessa multiplicação é ${multiplyResult}`)

console.log ("O resultado será", multiply(2,3)) //aqui to chamando a funcao dentro do console-log

